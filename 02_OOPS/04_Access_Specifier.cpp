/*
    Access Specifier are keywords which is used to define how the members of the class are accessible

    Types:
        1. public: attributes & methods can be accessible outside the class
        2. private: attributes & methods cannot be accessible outside the class
        3. protected: attributes & methods cannot be accessed outside the class, but can be accessed inside the inherited class

    Note:
        - if no access specifier is given, then members are made 'private'
*/